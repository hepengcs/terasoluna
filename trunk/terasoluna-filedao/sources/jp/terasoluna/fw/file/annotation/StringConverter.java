/**
 * 文字変換処理インタフェース
 *
 * <p>
 * 指定された文字列を対象に、大文字変換・小文字変換・無変換を行う。<br>
 * </p>
 *
 * 
 */
public interface StringConverter {

    /**
     * 文字列を変換する。
     * 
     * @param s
     *            変換前の文字列
     * @return 変換後の文字列
     */
    String convert(String s);

}
